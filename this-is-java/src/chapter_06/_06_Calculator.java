package chapter_06;

public class _06_Calculator {
    void powerOn(){
        System.out.println("Power On");
    }
    int plus(int x, int y){
        int result = x + y;
        return result;
    }
    int minus(int x, int y){
        int result = x - y;
        return result;
    }
    int multiple (int x, int y){
        int result = x * y;
        return result;
    }
    int devide (int x, int y){
        int result = x / y;
        return result;
    }
    void powerOff(){
        System.out.println("Power Off");
    }
}
