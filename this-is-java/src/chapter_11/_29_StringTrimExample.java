package chapter_11;

public class _29_StringTrimExample {
    public static void main(String[] args) {
        String tell1 = "   02";
        String tell2 = "49   ";
        String tell3 = "  68  ";
        String tell4 = "  102  2308   ";

        System.out.println(tell1.trim());
        System.out.println(tell2.trim());
        System.out.println(tell3.trim());
        System.out.println(tell4.trim());
    }
}
